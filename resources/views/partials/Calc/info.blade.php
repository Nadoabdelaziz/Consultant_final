                    بعد أن تبيّن أنّ بعض وجوه الإعجاز القرآني يقوم على حساب الجُمّل. ونظراً لتناقض مواقف العلماء من هذا
                    الحساب، فقد رأينا نعرّف القارئ الكريم بهذا الحساب، مع ملاحظة أننا لا نهتم بما تمّ اضافته من وجوه
                    لهذا الحساب بعد نزول القرآن الكريم، لأننا نرى أن اللغة العربيّة التي يُفسّر بها القرآن الكريم هي ما
                    كان عند نزول القرآن الكريم، لا ما استحدث من معاني لم تكن مرادة عند نزول القرآن الكريم. وكذلك الأمر
                    بالنسبة لحساب الجمّل الذي هو أيضاً لغة عرفها العرب قبل الإسلام. عليه فإنّ حساب الجمّل هو جزءٌ لا
